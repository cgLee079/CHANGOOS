package com.cglee079.changoos.model;

public class PhotoVo {
	private int seq;
	private String photoPath;
	private String photoPathname;
	private String snapshotPath;
	private String snapshotPathname;
	private String filename;
	private String name;
	private String desc;
	private String location;
	private String date;
	private String time;
	private String tag;
	private String device;
	private int likeCnt;

	public int getSeq() {
		return seq;
	}

	public void setSeq(int seq) {
		this.seq = seq;
	}

	public String getPhotoPath() {
		return photoPath;
	}

	public void setPhotoPath(String photoPath) {
		this.photoPath = photoPath;
	}

	public String getPhotoPathname() {
		return photoPathname;
	}

	public void setPhotoPathname(String photoPathname) {
		this.photoPathname = photoPathname;
	}

	public String getSnapshotPath() {
		return snapshotPath;
	}

	public void setSnapshotPath(String snapshotPath) {
		this.snapshotPath = snapshotPath;
	}

	public String getSnapshotPathname() {
		return snapshotPathname;
	}

	public void setSnapshotPathname(String snapshotPathname) {
		this.snapshotPathname = snapshotPathname;
	}

	public String getFilename() {
		return filename;
	}

	public void setFilename(String filename) {
		this.filename = filename;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public String getDevice() {
		return device;
	}

	public void setDevice(String device) {
		this.device = device;
	}

	public int getLikeCnt() {
		return likeCnt;
	}

	public void setLikeCnt(int likeCnt) {
		this.likeCnt = likeCnt;
	}

}
